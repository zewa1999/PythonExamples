# Valorile boolene le folosim in anumite conditii ce le vom invata mai tarziu

# Conversie de la un element de tip intreg la bool

print(bool(0)) # Se va afisa false. 0 este fals
print(bool(2)) # Orice valoare diferita de 0 este adevarata
print(bool(-65)) # Inclusiv cele cu minus

# Atentie, daca facem astfel de lucruri ca in print-urile de mai sus sa nu uitam de a face conversia!