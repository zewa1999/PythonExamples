# Definirea unei functii se face prin intermediul cuvantului def
# format: def nume_functie(parametrii):
# functiile respecta paradigma DRY -> Nu te repeta

def  spune_buna():
    print("Salut!")

# Apelam functia
# Obs! Nu putem apela o functie inainte ca aceasta sa fie definita!!
spune_buna()

# Afisarea adresei de memorie unde este salvata o functie

print(spune_buna())