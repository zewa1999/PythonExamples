# Urmatorii sunt operatori logici:
# and, or, not, >, <, ==, >=, <=, !=
# Exemple:

print(4>5)
print(4<5)
print(4 == 5)
print("salut" == "salut")
print('a' > 'b') # False
print('a' > 'A') # True
print(5>=5)
print(5<=6)
print(0 != 0) # != inseamna nu e egal
print(not True) # negatie
